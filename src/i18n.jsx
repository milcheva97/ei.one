const dictionaries = { de }
const originalText = new WeakMap()
const originalAttrs = new WeakMap()

function translateString(value, language) {
  if (language === 'en') return value

  const dictionary = dictionaries[language] || {}
  const normalized = value.replace(/\s+/g, ' ').trim()
  const translated = dictionary[normalized]

  if (!translated) return value

  const leading = value.match(/^\s*/)?.[0] || ''
  const trailing = value.match(/\s*$/)?.[0] || ''
  return `${leading}${translated}${trailing}`
}

function shouldSkipNode(node) {
  const parent = node.parentElement
  if (!parent) return true
  return ['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE', 'PRE'].includes(parent.tagName)
}

function translateDocument(language) {
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT)
  const textNodes = []

  while (walker.nextNode()) {
    textNodes.push(walker.currentNode)
  }

  textNodes.forEach((node) => {
    if (shouldSkipNode(node) || !node.nodeValue.trim()) return
    if (!originalText.has(node)) {
      originalText.set(node, node.nodeValue)
    }
    const nextValue = translateString(originalText.get(node), language)
    if (node.nodeValue !== nextValue) {
      node.nodeValue = nextValue
    }
  })

  const attrs = ['placeholder', 'aria-label', 'alt', 'title', 'value']
  document.querySelectorAll('*').forEach((element) => {
    attrs.forEach((attr) => {
      if (!element.hasAttribute(attr)) return
      let elementAttrs = originalAttrs.get(element)
      if (!elementAttrs) {
        elementAttrs = {}
        originalAttrs.set(element, elementAttrs)
      }
      if (!elementAttrs[attr]) {
        elementAttrs[attr] = element.getAttribute(attr)
      }
      const nextValue = translateString(elementAttrs[attr], language)
      if (element.getAttribute(attr) !== nextValue) {
        element.setAttribute(attr, nextValue)
      }
    })
  })
}

const LanguageContext = createContext(null)

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(() => {
    const stored = localStorage.getItem(STORAGE_KEY)
    return ['de', 'en'].includes(stored) ? stored : 'en'
  })

  const setLanguage = (nextLanguage) => {
    const normalized = nextLanguage === 'de' ? 'de' : 'en'
    localStorage.setItem(STORAGE_KEY, normalized)
    setLanguageState(normalized)
  }

  useEffect(() => {
    let frame = requestAnimationFrame(() => translateDocument(language))

    const scheduleTranslate = () => {
      cancelAnimationFrame(frame)
      frame = requestAnimationFrame(() => translateDocument(language))
    }

    const observer = new MutationObserver(scheduleTranslate)
    observer.observe(document.body, { childList: true, subtree: true })

    return () => {
      cancelAnimationFrame(frame)
      observer.disconnect()
    }
  }, [language])

  const value = useMemo(() => ({ language, setLanguage }), [language])

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used inside LanguageProvider')
  }
  return context
}
